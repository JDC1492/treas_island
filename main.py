direction = input(
    "You arrive at a fork in the path. Do you go left or right?\n").lower()
if (direction == "left"):
  action = input("You come to a lake. There is an island in the middle of the lake. Type 'wait' for a boat. Type 'swim' to swim across.\n").lower()
  if action == "wait":
    door = input("You arrive to the distant island unharmed. There's a house with 3 doors of the colors red yellow and blue. Which do you choose?\n").lower()
    if door == "red":
      print("You've been burned by fire. Game Over.")
    elif door == "blue":
      print("You've been eaten by beasts. Game Over.")
    elif door == "yellow":
      print('You Win!')
    else:
      print("Game Over.")
